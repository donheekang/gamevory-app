// 태그 배열 한국어 변환
export const translateTags = (tags) => {
  if (!tags || !Array.isArray(tags)) return [];
  return tags.map(t => {
    const name = typeof t === "string" ? t : t.name;
    return TAG_KO[name] || name;
  });
};
