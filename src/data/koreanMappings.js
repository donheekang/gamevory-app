// 한국어 제목 찾기 (slug 기반 → name 기반 순서)
export const getKoreanTitle = (game) => {
  if (!game) return null;

  // 1. slug으로 매핑 찾기
  if (game.slug && KOREAN_TITLES[game.slug]) {
    return KOREAN_TITLES[game.slug];
  }

  // 2. 이름을 slug 형태로 변환해서 찾기
  if (game.name || game.title) {
    const name = (game.name || game.title).toLowerCase()
      .replace(/[^a-z0-9\s-]/g, "")
      .replace(/\s+/g, "-")
      .replace(/-+/g, "-")
      .trim();
    if (KOREAN_TITLES[name]) return KOREAN_TITLES[name];
  }

  return null;
};

// 장르 한국어 변환
export const translateGenre = (genreName) => {
  return GENRE_KO[genreName] || genreName;
};

// 장르 배열 한국어 변환
export const translateGenres = (genres) => {
  if (!genres) return "";
  if (typeof genres === "string") {
    return genres.split(" / ").map(g => GENRE_KO[g.trim()] || g.trim()).join(" / ");
  }
  if (Array.isArray(genres)) {
    return genres.map(g => {
      const name = typeof g === "string" ? g : g.name;
      return GENRE_KO[name] || name;
    }).join(" / ");
  }
  return genres;
};

